/**
 * Filters colleges by a search query (case-insensitive substring match).
 * Returns at most `limit` results.
 */
export function filterColleges(query: string, limit = 10): CollegeEntry[] {
  if (query.length < 2) return [];

  const lowerQuery = query.toLowerCase();
  const results: CollegeEntry[] = [];

  for (const college of US_COLLEGES) {
    if (college.name.toLowerCase().includes(lowerQuery)) {
      results.push(college);
      if (results.length >= limit) break;
    }
  }

  return results;
}
